# Comparism Operators

# == equal to
# != not equal to 
# < less than
# > greater than
# <= less than or equal to
# >= greater than or equal to


# Examples
print(3 == 3) #True
print(3 == 4) #False
print(3 != 4) #True
print(3 != 3) #False
print(3 < 4) #True
print(3 > 4) #False
print(3 <= 4) #True
print(3 >= 4) #False
